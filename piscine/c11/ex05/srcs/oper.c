int	add(int s1, int s2)
{
	return (s1 + s2);
}

int	sub(int s1, int s2)
{
	return (s1 - s2);
}

int	div(int s1, int s2)
{
	return (s1 / s2);
}

int	mul(int s1, int s2)
{
	return (s1 * s2);
}

int	mod(int s1, int s2)
{
	return (s1 % s2);
}
